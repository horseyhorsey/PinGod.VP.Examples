/// <summary>
/// Used by <see cref="Switch"/>
/// </summary>
public enum BallSearchSignalOption
{
    /// <summary>
    /// Does nothing
    /// </summary>
    None,
    /// <summary>
    /// Resets the ball search
    /// </summary>
    Reset,
    /// <summary>
    /// Turns ball search off
    /// </summary>
    Off,
    /// <summary>
    /// Turns ball search on
    /// </summary>
    On
}
